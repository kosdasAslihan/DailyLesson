package ders06_increment_decrement;

public class C01_Increment {
    public static void main(String[] args) {

        int sayi=10;
        System.out.println( sayi = sayi+3); // 13

        System.out.println(sayi); // 13

        System.out.println(sayi-=5); // 8

        System.out.println(sayi);

        System.out.println(sayi++); // 8

        System.out.println(sayi);

        // once level'i atla sonra level degerini artir
        // once level degerini artir, sonra level atlat


    }
}
